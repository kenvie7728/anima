package com.zxc.fight.bean;

public class Role {
    
	private int x;
	private int y;
	private int blood;
	private int magic;
	private int atack;
	private boolean defence;
	
	
	public int getX() {
		return x;
	}


	public void setX(int x) {
		this.x = x;
	}


	public int getY() {
		return y;
	}


	public void setY(int y) {
		this.y = y;
	}


	public int getBlood() {
		return blood;
	}


	public void setBlood(int blood) {
		this.blood = blood;
	}


	public int getMagic() {
		return magic;
	}


	public void setMagic(int magic) {
		this.magic = magic;
	}


	public int getAtack() {
		return atack;
	}


	public void setAtack(int atack) {
		this.atack = atack;
	}


	public boolean isDefence() {
		return defence;
	}


	public void setDefence(boolean defence) {
		this.defence = defence;
	}


	public Role(){
    	   
    }
	
	
	
}
